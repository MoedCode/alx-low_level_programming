#include "main.h"

/**
 * add - a function that adds two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function that subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - a function that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: the product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - a function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: the quotient of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - a function that finds the remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: the remainder of the division of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
